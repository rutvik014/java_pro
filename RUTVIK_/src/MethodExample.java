public class MethodExample {
    public static void main(String[] args) {
        // Calling the method and storing its result in a variable
        int result = methodExample(5, 3);

        // Printing the result
        System.out.println("Result: " + result);
    }

    // Definition of the method named methodExample
    public static int methodExample(int a, int b) {
        // This method takes two integers (a and b) as parameters
        // and returns their sum as an integer
        return a + b;
    }
}
